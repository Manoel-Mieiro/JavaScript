let x = 10;

x += 2;         // x = x + 2
console.log(x); // -> 12
x -= 4;         // x = x - 4 -> x = 12 - 4 -> x = 8
console.log(x); // -> 8
x *= 3;         // x = 8 * 3 -> x = 24
console.log(x); // -> 24
x /= 6;         // x = 24/6 -> x = 4
console.log(x); // -> 4
x **= 3;        // x = 4**3 -> x = 64
console.log(x); // -> 64
x %= 10;        // x = 64%10 -> 4
console.log(x); // -> 4
